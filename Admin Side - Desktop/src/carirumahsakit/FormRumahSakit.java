    private void load_table(){
        // membuat tampilan model tabel
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Kecamatan");
        model.addColumn("Nama RS");
        model.addColumn("Alamat");
        model.addColumn("No Telp");
        model.addColumn("Latitude");
        model.addColumn("Longitude");
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "select * from rumah_sakit";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7)});
            }
            tableRS.setModel(model);
        } catch (Exception e) {
        }
    }
    
