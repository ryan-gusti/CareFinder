    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        try {
            String sql = "INSERT INTO rumah_sakit VALUES ('"+input_id.getText()+"','"+inputKec.getSelectedIndex()+"','"+inputNama.getText()+"','"+inputAlamat.getText()+"','"+inputNotelp.getText()+"','"+inputLat.getText()+"','"+inputLong.getText()+"')";
            java.sql.Connection conn=(Connection)config.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data RS berhasil ditambahkan!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        load_table();
        reset();
    }//GEN-LAST:event_btn_simpanActionPerformed

    private void tableRSMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableRSMouseClicked
        input_id.setText(tableRS.getValueAt(tableRS.getSelectedRow(), 0).toString());
        int id = Integer.parseInt(tableRS.getValueAt(tableRS.getSelectedRow(), 1).toString()); 
        inputKec.setSelectedIndex(id);
        inputNama.setText(tableRS.getValueAt(tableRS.getSelectedRow(), 2).toString());
        inputNotelp.setText(tableRS.getValueAt(tableRS.getSelectedRow(), 4).toString());
        inputLat.setText(tableRS.getValueAt(tableRS.getSelectedRow(), 5).toString());
        inputLong.setText(tableRS.getValueAt(tableRS.getSelectedRow(), 6).toString());
        inputAlamat.setText(tableRS.getValueAt(tableRS.getSelectedRow(), 3).toString());
        btn_simpan.setEnabled(false);
        btn_edit.setEnabled(true);
        btn_hapus.setEnabled(true);
    }//GEN-LAST:event_tableRSMouseClicked

